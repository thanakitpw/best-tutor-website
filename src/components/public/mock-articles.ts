// Helpers for article authoring — reduce verbosity and keep typos rare.

const h2 = (text: string): TiptapHeading => ({
  type: "heading",
  attrs: { level: 2 },
  content: [{ type: "text", text }],
});

const h3 = (text: string): TiptapHeading => ({
  type: "heading",
  attrs: { level: 3 },
  content: [{ type: "text", text }],
});

const p = (...children: TiptapInline[]): TiptapParagraph => ({
  type: "paragraph",
  content: children,
});

const t = (text: string): TiptapText => ({ type: "text", text });

const bold = (text: string): TiptapText => ({
  type: "text",
  text,
  marks: [{ type: "bold" }],
});

const link = (text: string, href: string): TiptapText => ({
  type: "text",
  text,
  marks: [{ type: "link", attrs: { href } }],
});

const ul = (...items: string[]): TiptapBulletList => ({
  type: "bulletList",
  content: items.map((item) => ({
    type: "listItem",
    content: [{ type: "paragraph", content: [{ type: "text", text: item }] }],
  })),
});

const ol = (...items: string[]): TiptapOrderedList => ({
  type: "orderedList",
  content: items.map((item) => ({
    type: "listItem",
    content: [{ type: "paragraph", content: [{ type: "text", text: item }] }],
  })),
});

const img = (src: string, alt: string): TiptapImage => ({
  type: "image",
  attrs: { src, alt },
});

const quote = (text: string): TiptapBlockquote => ({
  type: "blockquote",
  content: [{ type: "paragraph", content: [{ type: "text", text }] }],
});

