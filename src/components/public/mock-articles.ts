// Tiptap JSON shape — intentionally hand-written so we don't pull in @tiptap
// into the public bundle. Matches Tiptap StarterKit output.

export type TiptapMark =
  | { type: "bold" }
  | { type: "italic" }
  | { type: "link"; attrs: { href: string; target?: string; rel?: string } };

export interface TiptapText {
  type: "text";
  text: string;
  marks?: TiptapMark[];
}

export type TiptapInline = TiptapText;

export interface TiptapHeading {
  type: "heading";
  attrs: { level: 1 | 2 | 3 | 4 };
  content?: TiptapInline[];
}

export interface TiptapParagraph {
  type: "paragraph";
  content?: TiptapInline[];
}

export interface TiptapListItem {
  type: "listItem";
  content?: TiptapBlock[];
}

export interface TiptapBulletList {
  type: "bulletList";
  content: TiptapListItem[];
}

export interface TiptapOrderedList {
  type: "orderedList";
  content: TiptapListItem[];
}

export interface TiptapImage {
  type: "image";
  attrs: { src: string; alt?: string; title?: string };
}

export interface TiptapBlockquote {
  type: "blockquote";
  content?: TiptapBlock[];
}

export interface TiptapCodeBlock {
  type: "codeBlock";
  attrs?: { language?: string };
  content?: TiptapInline[];
}

export type TiptapBlock =
  | TiptapHeading
  | TiptapParagraph
  | TiptapBulletList
  | TiptapOrderedList
  | TiptapImage
  | TiptapBlockquote
  | TiptapCodeBlock;

export interface TiptapDoc {
  type: "doc";
  content: TiptapBlock[];
}

