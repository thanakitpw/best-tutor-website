// Helpers — keep lookup logic co-located with the data.

/** Find a single article by slug — case-sensitive match. */
export function getArticleBySlug(slug: string): MockArticle | undefined {
  return MOCK_ARTICLES.find((article) => article.slug === slug);
}

/** Get articles in a category, sorted by `publishedAt` desc. */
export function getArticlesByCategory(categorySlug: string): MockArticle[] {
  return MOCK_ARTICLES.filter((a) => a.category === categorySlug).sort(
    (a, b) => b.publishedAt.localeCompare(a.publishedAt),
  );
}

/** Return all articles sorted newest first. */
export function getAllArticles(): MockArticle[] {
  return [...MOCK_ARTICLES].sort((a, b) =>
    b.publishedAt.localeCompare(a.publishedAt),
  );
}

/** Related articles — same category (excluding self), falls back to newest
 * from other categories if fewer than `limit` siblings exist. */
export function getRelatedArticles(
  article: MockArticle,
  limit = 3,
): MockArticle[] {
  const sameCategory = MOCK_ARTICLES.filter(
    (a) => a.category === article.category && a.slug !== article.slug,
  );
  const pool =
    sameCategory.length >= limit
      ? sameCategory
      : [
          ...sameCategory,
          ...MOCK_ARTICLES.filter(
            (a) =>
              a.category !== article.category && a.slug !== article.slug,
          ),
        ];
  return pool
    .sort((a, b) => b.publishedAt.localeCompare(a.publishedAt))
    .slice(0, limit);
}

/** Find category meta by slug; defaults to the generic bucket. */
export function findCategory(slug: string): BlogCategory | undefined {
  return BLOG_CATEGORIES.find((c) => c.slug === slug);
}

/** Count articles per category — used by the sidebar filter. */
export function countArticlesByCategory(): Record<string, number> {
  const counts: Record<string, number> = {};
  for (const cat of BLOG_CATEGORIES) counts[cat.slug] = 0;
  for (const article of MOCK_ARTICLES) {
    counts[article.category] = (counts[article.category] ?? 0) + 1;
  }
  return counts;
}
